# –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –º–æ–¥—É–ª—å –¥–ª—è —Ä–∞–Ω–¥–æ–º–∞
import random

passSymbols = [" ", "!", "'", "$", "%", "(", "&", ")", "[", "]", "{", "}", "?", "/", "|", ".", ",", "*", "^", ";", ":", "<", ">", "#", "@", "+", "_", "-", "=", "`", "~"]

passAlpMini = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j", "k", "l", "m", "n", "o", "p", "q", "r", "s", "t", "u", "v", "w", "x", "y", "z"]

# –ú–∞—Å—Å–∏–≤ –¥–ª—è –±–æ–ª—å—à–∏—Ö –±—É–∫–≤ –ø–æ–ª—É—á–∞–µ–º –∏–∑ –º–∞–ª–µ–Ω—å–∫–∏—Ö –º–µ—Ç–æ–¥–æ–º upper()
passAlp = [char.upper() for char in passAlpMini]

# –ú–∞—Å—Å–∏–≤ –¥–ª—è —Ü–∏—Ñ—Ä
passNum = [1, 2, 3, 4, 5, 6, 7, 8, 9, 0]

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –¥–ª–∏–Ω—É –ø–∞—Ä–æ–ª—è
passLength = int(input("–î–ª–∏–Ω–∞ –≤–∞—à–µ–≥–æ –ø–∞—Ä–æ–ª—è: "))

# –±–µ—Ä–µ–º –ø–æ —Ä–∞–Ω–¥–æ–º—É —Å–∏–º–≤–æ–ª—ã –∏–∑ –≤—Å–µ—Ö –º–∞—Å—Å–∏–≤–æ–≤ –¥–ª—è –ø–∞—Ä–æ–ª—è
password = ''.join([str(random.choice(passAlpMini + passAlp + passNum + passSymbols))
                   for i in range(passLength)])

# –í—ã–≤–æ–¥–∏–º –Ω–∞—à —Å–æ—á–Ω—ã–π –ø–∞—Ä–æ–ª—å
print("–ü–∞—Ä–æ–ª—åüëâ: ", password)
